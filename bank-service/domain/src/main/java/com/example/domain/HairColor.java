package com.example.domain;

/**
 * Перечисление доступных цветов волос.
 */
public enum HairColor {
    /**
     * Чёрный цвет волос.
     */
    BLACK,

    /**
     * Коричневый цвет волос.
     */
    BROWN,

    /**
     * Блонд (светлый) цвет волос.
     */
    BLOND,

    /**
     * Рыжий цвет волос.
     */
    RED,

    /**
     * Серый цвет волос.
     */
    GREY;
}